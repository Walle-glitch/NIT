matris = []                 # börja med att skapa en tom lista
for i in range(1, 4):       # låt i anta värdena 1, 2, 3
  matris.append([])         # lägg till en ny rad (en ny, tom lista i listan)
  for j in range(1, 6):     # låt j anta värdena 1, 2, 3, 4, 5
    matris[-1].append()  # lägg till på (hittills) sista raden: VAD?
print(matris)
